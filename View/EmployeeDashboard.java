    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
       try {
    // READ INPUTS
    String model = jTextPane1.getText().trim();
    String plate = jTextPane2.getText().trim();
    String mileageText = jTextPane3.getText().trim();
    String priceText = jTextPane4.getText().trim();
    String availability = jTextPane5.getText().trim();

    
    if (model.isEmpty() || plate.isEmpty() || mileageText.isEmpty()
            || priceText.isEmpty() || availability.isEmpty()) {

        javax.swing.JOptionPane.showMessageDialog(
            null,
            "⚠ Please fill all fields!",
            "Missing Data",
            javax.swing.JOptionPane.WARNING_MESSAGE
        );
        return;
    }

   
    int mileage = Integer.parseInt(mileageText);
    double price = Double.parseDouble(priceText);

    // CREATE VEHICLE OBJECT
    Vehicle vehicle = new Vehicle(
        0,
        model,
        plate,
        mileage,
        price,
        availability
    );

    VehicleController controller = new VehicleController();
    controller.addVehicle(vehicle);

    // SUCCESS MESSAGE
    javax.swing.JOptionPane.showMessageDialog(
        null,
        "✔ Vehicle Added Successfully!",
        "Success",
        javax.swing.JOptionPane.INFORMATION_MESSAGE
    );

} catch (NumberFormatException e) {

    javax.swing.JOptionPane.showMessageDialog(
        null,
        "❌ Mileage and Price must be numbers!",
        "Input Error",
        javax.swing.JOptionPane.ERROR_MESSAGE
    );

} catch (Exception e) {

    javax.swing.JOptionPane.showMessageDialog(
        null,
        "Unexpected error: " + e.getMessage(),
        "Error",
        javax.swing.JOptionPane.ERROR_MESSAGE
    );
}
    }//GEN-LAST:event_jButton10ActionPerformed

