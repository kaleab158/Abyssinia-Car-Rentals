 private void loadCustomers() {

    CustomerController controller =
            new CustomerController();

    List<Customer> customers =
            controller.getAllCustomers();

    DefaultTableModel model =
            (DefaultTableModel) tblCustomers.getModel();

    model.setRowCount(0);

    for (Customer c : customers) {

        model.addRow(new Object[]{

            c.getCustomerId(),
            c.getFirstName(),
            c.getLastName(),
            c.getCity(),
            c.getCountry(),
            c.getContactNumber(),
            c.getLicenseNumber()

        });
    }
}
 private void loadReservations1() {

    ReservationController controller =
            new ReservationController();

    VehicleDAO vehicleDAO =
            new VehicleDAO();

    List<Reservation> list =
            controller.getAllReservations();

    DefaultTableModel model =
            (DefaultTableModel) tblReservations2.getModel();

    model.setRowCount(0);

    for (Reservation r : list) {

        String vehicleModel =
                vehicleDAO.getVehicleModel(
                        r.getVehId());

        model.addRow(new Object[]{

            r.getReservationId(),
            r.getCustomerId(),
            vehicleModel,
            r.getReserveDate(),
            r.getReturnDate()

        });
    }}
    public void loadVehicles() {
    VehicleController controller = new VehicleController();
     
    ArrayList<Vehicle> vehicles = (ArrayList<Vehicle>) controller.getAllVehicles();
           
    DefaultTableModel model = (DefaultTableModel) tblVehicles.getModel();
            
    model.setRowCount(0);

    for (Vehicle v : vehicles) {

        model.addRow(new Object[] {

            v.getVehId(),
            v.getModel(),
            v.getPlateNo(),
            v.getMileAge(),
            v.getDailyPrice(),
            v.getAvailability()

        });
    }}
