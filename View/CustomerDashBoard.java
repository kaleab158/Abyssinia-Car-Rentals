   public void loadVehiclesID(int vehicleId) {
    VehicleController controller = new VehicleController();

    Vehicle v = controller.getVehicleById(vehicleId);

    DefaultTableModel model = (DefaultTableModel) SearchTbl.getModel();

    model.setRowCount(0);

    if (v != null) {

        model.addRow(new Object[]{
            v.getVehId(),
            v.getModel(),
            v.getPlateNo(),
            v.getMileAge(),
            v.getDailyPrice(),
            v.getAvailability()
        });

    } else {
        JOptionPane.showMessageDialog(this, "Vehicle not found!");
    }}
